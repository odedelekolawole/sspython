"""
Operations in Booleans
"""

# - not
# - and
# - or

"""
Not Operator
"""
it_is_raining = True
print(not it_is_raining)

"""
and Operator
"""

it_is_sunny = True
print(it_is_raining and it_is_sunny)

print(it_is_raining == it_is_sunny)

"""
or Operator
"""
print(it_is_raining or it_is_sunny)


"""
Symbol for boolen keyword
"""


print(it_is_raining & it_is_sunny)
