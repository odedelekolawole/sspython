#   Math Functions

def add(x, y):
    """Adds two integers x and y

    Args:
        x (int): integers
        y (int): integers

    Returns:
        int: Sum of x and y
    """
    return x + y

def subtract(x, y):
    """difference between two integers x and y

    Args:
        x (int): integers
        y (int): integers

    Returns:
        int: Difference bewtween x and y
    """
    return x - y

def multiply(x, y):
    """product of two integers x and y

    Args:
        x (int): integers
        y (int): integers

    Returns:
        int: Product of x and y
    """
    return x * y

