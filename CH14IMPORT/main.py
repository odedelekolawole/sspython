import mathstuf

number1 = 12
number2 = 13

total = mathstuf.add(number1, number2)

print(f"total = {total}")

subtraction = mathstuf.subtract(number2, number1)
print(f"Substraction Result = {subtraction}")

multiplication = mathstuf.multiply(number1, number2)
print(f"Multiplication Result = {multiplication}")